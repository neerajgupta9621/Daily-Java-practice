class MathFun {
    public static void main(String[] args) {
        System.out.println(Math.abs(-21));
        System.out.println(Math.PI);
        System.out.println(Math.ceil(10.5));
        System.out.println(Math.floor(20.5));
        System.out.println(Math.round(21.0));
        System.out.println(Math.pow(2, 4));
        System.out.println(Math.sqrt(2));
        System.out.println(Math.random());
        System.out.println(Math.exp(5));
        System.out.println(Math.log(5));
        System.out.println(Math.sin(4));
        System.out.println(Math.tan(3));
        System.out.println(Math.cos(8));

    }
}
